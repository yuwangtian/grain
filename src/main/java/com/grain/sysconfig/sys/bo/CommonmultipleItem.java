package com.grain.sysconfig.sys.bo;

/**
 * 公用下拉多选元素
 *
 * @author wzy
 * @since 2014-11-26 19:43:33
 */

public class CommonmultipleItem {
    private String id;
    private String text;
    private String desc;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }


}
